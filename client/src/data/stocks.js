// Helper function to generate realistic stock data
export const generateStockData = (stock) => {
  const basePrice = stock.basePrice;
  const volatility = 0.02 + Math.random() * 0.03; // 2-5% volatility
  const changePercent = (Math.random() - 0.5) * 2 * volatility * 100;
  const change = basePrice * (changePercent / 100);
  const price = basePrice + change;

  return {
    symbol: stock.symbol,
    name: stock.name,
    sector: stock.sector,
    price: Math.round(price * 100) / 100,
    change: Math.round(change * 100) / 100,
    changePercent: Math.round(changePercent * 100) / 100,
    volume: Math.floor(Math.random() * 50000000) + 100000,
    marketCap: Math.round(price * (Math.random() * 100 + 10) * 1000000000),
    dayHigh: Math.round((price + Math.random() * price * 0.02) * 100) / 100,
    dayLow: Math.round((price - Math.random() * price * 0.02) * 100) / 100,
    previousClose: Math.round((price - change) * 100) / 100,
    timestamp: new Date().toISOString()
  };
};

// Get all stock symbols
export const getAllSymbols = () => stocks.map(s => s.symbol);

// Search stocks by symbol or name
export const searchStocks = (query) => {
  if (!query || query.length < 1) return [];

  const searchLower = query.toLowerCase();
  return stocks.filter(stock =>
    stock.symbol.toLowerCase().includes(searchLower) ||
    stock.name.toLowerCase().includes(searchLower)
  ).slice(0, 20);
};

// Get stock by symbol
export const getStockBySymbol = (symbol) => {
  return stocks.find(s => s.symbol === symbol.toUpperCase());
};

// Get stocks by sector
export const getStocksBySector = (sector) => {
  return stocks.filter(s => s.sector === sector);
};

// Get all sectors
export const getSectors = () => {
  return [...new Set(stocks.map(s => s.sector))];
};

